# Ask for the dog's name, store in variable
dog_name = input("What is your dog's name? ")

# Ask for the dog's age, store in variable
dog_age = input("What is your dog's age? ")

# Dog's age * 7, store in variable
human_age = int(dog_age) * 7

# Return "Your dog, [name], is [years] years old in human years"
print("Your dog, ", dog_name, ", is ", human_age, " years old in human years. ")
